class Animal {
    constructor(
        id,
        name,
        imageUrl,
        breed,
        color,
        size,
        status,
        sex
    ) {
        this.id = id,
            this.name = name,
            this.imageUrl = imageUrl,
            this.breed = breed,
            this.color = color,
            this.size = size,
            this.status = status,
            this.sex = sex
    }
}

export default Animal